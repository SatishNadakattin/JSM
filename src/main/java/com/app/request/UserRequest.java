package com.app.request;

import javax.persistence.Column;

public class UserRequest {
	private String username;
	private String password;
	private String mobile;
	private String emailId;
	private String address;
	private String name;
	private String CreatedBy;
	
	private String oldPassword;
	private String newPassword;
	private String confirmPassword;
	
	private String ModifiedBy;  
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCreatedBy() {
		return CreatedBy;
	}
	public void setCreatedBy(String createdBy) {
		CreatedBy = createdBy;
	}
	public String getModifiedBy() {
		return ModifiedBy;
	}
	public void setModifiedBy(String modifiedBy) {
		ModifiedBy = modifiedBy;
	}
	public String getOldPassword() {
		return oldPassword;
	}
	public void setOldPassword(String oldPassword) {
		this.oldPassword = oldPassword;
	}
	public String getNewPassword() {
		return newPassword;
	}
	public void setNewPassword(String newPassword) {
		this.newPassword = newPassword;
	}
	public String getConfirmPassword() {
		return confirmPassword;
	}
	public void setConfirmPassword(String confirmPassword) {
		this.confirmPassword = confirmPassword;
	}
	@Override
	public String toString() {
		return "UserRequest [username=" + username + ", password=" + password + ", mobile=" + mobile + ", emailId="
				+ emailId + ", address=" + address + ", name=" + name + ", CreatedBy=" + CreatedBy + ", oldPassword="
				+ oldPassword + ", newPassword=" + newPassword + ", confirmPassword=" + confirmPassword
				+ ", ModifiedBy=" + ModifiedBy + "]";
	}
	
	

	
}